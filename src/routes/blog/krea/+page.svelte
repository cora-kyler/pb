	<p>Picture it: October, 2022. San Francisco, CA. The aftershocks of COVID are still damping face-to-face socialization. "ChatGPT" is more than a month away from being A Thing. We at Pebblebed are a few months into deploying our maiden fund, amidst investor sentiment on AI ranging from apathy to eye-rolls. Two Spanish guys with impeccable personal style and an all-consuming mission to fuse human creativity with the burgeoning capabilities of GenAI first swung by to Pebblebed HQ to talk to us about their startup, KREA.</p>
	
	<p>Diego and Victor's brilliance was immediately palpable, and we learned they had turned their lives inside out in every imaginable way to bring their company to life. The product at this point was still in an early form; it was a tasteful take on text-guided image generation, with a well-executed infinite canvas as its central element. We were excited to partner with KREA in part because of the promise of what they had built so far, but mostly because of who Diego and Victor are: two brilliant young people who understand in the marrow of their bones what artists require from an AI-first tool, because they need that tool themselves. So we hauled a laptop out to Golden Gate Park's panhandle, a few blocks from the first KREAhaus, and shook hands on Pebblebed's commitment to KREA over a Google Sheet on a park bench.</p>

	<h2 class="blog-h2">How KREA Wins</h2>
	
	<p>From that day in 2022 when we first met Victor and Diego, and no doubt before then, KREA has been moving from strength to strength. When they first demo'ed their groundbreaking <a href="https://www.loom.com/share/c358a3f8103045f2b4efac7eaa3dbf9e?t=14" target="_blank" rel="noopener noreferrer">realtime</a> capability to us in Fall 2023, it was one of a handful of times in Pebblebed's collective 50+ years in the technology industry where a demo left us with literal goosebumps. We had witnessed something <strong>Promethean.</strong> It was a unique fusion of product sense and applied AI research, where a computer was simultaneously doing something <em>obviously useful</em> and <em>never before seen</em>. If you have not yet used it, stop reading and play with it <a href="https://krea.ai" target="_blank" rel="noopener noreferrer">right. Now.</a></p>
	
	<p>The launch of KREA realtime started a cascade of user growth that continues to this day, fueled by a torrid pace of new features, integrated tastefully. Staying ahead of the ever-accelerating treadmill of scaling challenges posed by usage growth; following the bleeding edge of GenAI capabilities; and peering beyond that edge into original research when called for, has required the talents of not only Victor and Diego, but the assembly of one of the most gifted, dedicated, and technically sharp teams in the industry.</p>
	
	<p>When we describe a young team packed with <em>technical</em> talent, the SF stereotype of 2025 suggests a quivering mass of <a href="https://www.youtube.com/watch?v=wmg3mLwHEBc&t=2s" target="_blank" rel="noopener noreferrer">overstimulated</a> keyboard warriors <a href="https://www.youtube.com/shorts/kAOuzkI9LsI" target="_blank" rel="noopener noreferrer">mainlining Huberman</a> life advice while churning out a gazillion miles of code. KREA contains multitudes, and they can measure up to that cartoon in their relentless grit and focus. But their talents extend beyond bending GPUs to their will: the team includes musicians, photographers, writers, graffiti artists. There is no algorithm for <em>taste</em>. This melange of creative and technical virtuosity in the same mind has been one of the keys to KREA's breathtaking speed. Ideas do not need to cross the boundary from a product person's brain into that of an engineer to be shipped, iterated, and refined, because the product people are also great builders.</p>

	<h2 class="blog-h2">KREA today and tomorrow</h2>
	
	<p>Millions of people have used KREA to date. They use it not just to generate images, but to sketch, test, iterate, <strong>dream.</strong> What began as a prototyping tool not even 18 months ago has already evolved into something much more powerful: a medium for expression that operates at the speed of imagination.</p>
	
	<p>KREA's mission is to collapse the distance between an idea and its realization. For most of history, only a small subset of people—trained artists, designers, animators—have had the means to do this. The rest of us relied on language, or kept those visions to ourselves. That boundary is starting to dissolve.</p>
	
	<p>What happens to culture when everyone can speak in visuals? When the pace of creation is indistinguishable from the pace of thought? We don't know. KREA is where that question begins to take shape.</p>
	
